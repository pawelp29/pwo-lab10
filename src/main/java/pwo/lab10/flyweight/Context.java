package pwo.lab10.flyweight;

/**
 *
 * @author pawelp
 */
public class Context {

    private double x, y;

    public Context(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
